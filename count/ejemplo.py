# Se crea una variable llamada 'texto' que contiene una cadena de caracteres
texto = 'julian Velez G'

# Se utiliza el método .count() para contar cuántas veces aparece la letra 'j' en el texto
# El método .count() distingue entre mayúsculas y minúsculas, así que solo cuenta letras 'j' minúsculas
print(texto.count('j'))  # Esto imprimirá 1 porque solo hay una 'j' en el texto

